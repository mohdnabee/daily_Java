package myFirstPrograms;

public class Operators {

    public static void main(String[] args) {
        int a =  10  , b =  5 ;

        System.out.println("Operators Java :-");

        System.out.println("\nArithmetic Operators:-");
        System.out.println("  Binary Operators:- ");
        System.out.println("  let A= 10 , B = 5");
        System.out.println("  + => A+B "+ (a+b));
        System.out.println("  - => A+B "+ (a-b));
        System.out.println("  * => A+B "+ (a*b));
        System.out.println("  / => A+B "+ (a/b));
        System.out.println("  % => A+B "+ (a%b));

        System.out.println("\nUnary Operators:- ");
        System.out.println("1. ++ => Increment Operator");
        System.out.println("2. -- => Decrement Operator");
        System.out.println( "\n* Increment Operator \n  .Pre Increment Operator = ++a  \n  .post Increment Operator = a++");


    }
}
