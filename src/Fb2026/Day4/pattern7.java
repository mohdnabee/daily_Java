package Fb2026.Day4;

public class pattern7 {
//      Inverted Half pyramid with Numbers
public static void main(String[] args) {


    int n= 5;

    for ( int i = n ; i>=1 ; i--){

        for ( int j = 1;  j<=i ; j++){
            System.out.print(j+ " ");
        }
        System.out.println();



    }

}
}
